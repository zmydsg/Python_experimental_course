def player(Colour, Board):
    """
    贪心玩家策略，每一步选择能翻转最多敌方棋子的落子位置
    """
    possible_moves = PossibleMove(Colour, Board)
    if not possible_moves:
        return (0, 0)

    best_move = None
    max_flips = -1

    for move in possible_moves:
        x, y = move
        new_board = BoardCopy(Board)
        new_board = PlaceMove(Colour, new_board, x, y)

        # 计算翻转的棋子数
        flips = sum([row.count(Colour) for row in new_board]) - sum([row.count(Colour) for row in Board])

        if flips > max_flips:
            max_flips = flips
            best_move = move

    return best_move
